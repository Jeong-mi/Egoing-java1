public class DataType{
	public static void main(String[] args) {
		System.out.println(6); //Number
		System.out.println("six"); //String
		
		System.out.println("6"); //String 6
		
		System.out.println(6+6); //12
		System.out.println("6"+"6"); //66 결합연산자 사용
		
		System.out.println(6*6); //36
		//System.out.println("6"*"6"); //error, 문자열은 곱하기 불가
		
		System.out.println("1111".length()); //4, 문자열의 길이를 알려주는 연산
		//System.out.println(1111.length()); //error, 숫자는 길이구할 수 없다
		//데이터타입에 어울리는 연산이 각각 존재한다.
		//어떤 존재의 데이터타입이 존재하는가 + 각각에 어울리는 연산
		
		
		
	}
}